import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const defaultDatabase = "/Users/brianneradt/src/myiosapps/bible-reader/BibleReader/Resources/bible.sqlite";
const database = process.env.BIBLE_SQLITE ?? defaultDatabase;
const output = resolve("src/data/scripture.generated.ts");

if (!existsSync(database)) {
  throw new Error(`Bible SQLite database not found at ${database}`);
}

const books = {
  1: { slug: "gen", name: "Genesis" },
  6: { slug: "josh", name: "Joshua" },
  8: { slug: "ruth", name: "Ruth" },
  9: { slug: "1-sam", name: "1 Samuel" },
  10: { slug: "2-sam", name: "2 Samuel" },
  11: { slug: "1-kgs", name: "1 Kings" },
  40: { slug: "matt", name: "Matthew" },
  42: { slug: "luke", name: "Luke" },
  58: { slug: "heb", name: "Hebrews" },
  59: { slug: "jas", name: "James" },
};

const chaptersToBundle = [
  [1, 2], [1, 4], [1, 5], [1, 6], [1, 7], [1, 8], [1, 9], [1, 10], [1, 11],
  [1, 12], [1, 13], [1, 15], [1, 16], [1, 17], [1, 18], [1, 19], [1, 20], [1, 21],
  [1, 22], [1, 24], [1, 25], [1, 26], [1, 27], [1, 28], [1, 29], [1, 30],
  [1, 31], [1, 32], [1, 33], [1, 34], [1, 35], [1, 37], [1, 38], [1, 39],
  [1, 41], [1, 42], [1, 43], [1, 44], [1, 45], [1, 46], [1, 48], [1, 49],
  [1, 50], [6, 2], [6, 6], [8, 1], [8, 2], [8, 3], [8, 4], [9, 16], [9, 17],
  [10, 7], [10, 11], [10, 12], [11, 3], [40, 1], [40, 2], [42, 1], [42, 2],
  [42, 3], [58, 11], [59, 2],
];

const predicates = chaptersToBundle.map(
  ([book, chapter]) => `(book_id=${book} AND chapter=${chapter})`,
);

const query = `
  SELECT resource_id, book_id, chapter, verse, text
  FROM translations
  WHERE resource_id IN ('web', 'kjv')
    AND (${predicates.join(" OR ")})
  ORDER BY book_id, chapter, verse, resource_id;
`;

const rows = JSON.parse(execFileSync("sqlite3", ["-json", database, query], { encoding: "utf8" }));
const verses = {};
const chapters = {};

for (const row of rows) {
  const book = books[row.book_id];
  if (!book) continue;
  const id = `${book.slug}-${row.chapter}-${row.verse}`;
  verses[id] ??= {
    id,
    reference: `${book.name} ${row.chapter}:${row.verse}`,
    web: "",
    kjv: "",
  };
  verses[id][row.resource_id] = row.text;
}

for (const verse of Object.values(verses)) {
  if (!verse.web || !verse.kjv) {
    throw new Error(`Missing a translation for ${verse.reference}`);
  }

  const chapterId = verse.id.slice(0, verse.id.lastIndexOf("-"));
  chapters[chapterId] ??= {
    id: chapterId,
    reference: verse.reference.split(":")[0],
    verseIds: [],
  };
  chapters[chapterId].verseIds.push(verse.id);
}

const source = `// Generated by scripts/generate-scripture.mjs from the vetted eBible-derived\n// WEB and KJV resources bundled with BibleReader. Do not edit by hand.\nimport type { Verse } from "./types";\n\nexport const scripture: Record<string, Verse> = ${JSON.stringify(verses, null, 2)};\n`;

const chapterSource = `\nexport const chapters: Record<string, import("./types").BibleChapter> = ${JSON.stringify(chapters, null, 2)};\n`;

mkdirSync(dirname(output), { recursive: true });
writeFileSync(output, source + chapterSource);
console.log(`Wrote ${Object.keys(verses).length} bilingual verses across ${Object.keys(chapters).length} chapters to ${output}`);
